    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // BOTON PARA INSERTAR
        try {
            Employee employee = new Employee();
            employee.setDocument(Integer.parseInt(jTextFieldDocument.getText()));
            employee.setFullName(jTextFieldFullName.getText());
            employee.setAddress(jTextFieldAddress.getText());
            employee.setPhone(jTextFieldPhone.getText());
            employee.setEmployeeType((EmployeeType) jComboBoxType_id.getSelectedItem());
            employeeController.insert(employee);
            MessageUtils.showInfoMessage("El empleado se ha añadido correctamente...");
            fillTable();
            clear();
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Ha ocurrido un error al insertar el usuario..."
                    + e.getMessage());
        }
    }//GEN-LAST:event_jButtonInsertActionPerformed

