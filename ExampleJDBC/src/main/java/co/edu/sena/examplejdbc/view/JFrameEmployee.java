    public void fillTable() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            jTableEmployee.setModel(model);
            model.addColumn("documento");
            model.addColumn("Nombre Completo");
            model.addColumn("Tipo de empleado");

            String[] rows = new String[3];
            List<Employee> types = employeeController.findAll();
            for (Employee type : types) {
                rows[0] = String.valueOf(type.getDocument());
                rows[1] = type.getFullName();
                rows[2] = type.getEmployeeType().getDescript();
                model.addRow(rows);
            }
            jButtonInsert.setEnabled(true);
            jButtonUpdate.setEnabled(false);
            jButtonDelete.setEnabled(false);
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Ha ocurrido un error al llenar la tabla..."
                    + e.getMessage());
        }
    }
    
    private void fillComboBox() {
        try {
            List<EmployeeType> allTypes = employeeTypeController.findAll();
            DefaultComboBoxModel model = new DefaultComboBoxModel();
            jComboBoxType_id.setModel(model);
            model.addAll(allTypes);
            jComboBoxType_id.setSelectedIndex(0);
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Ha ocurrido Error al llenar el ComboBox: " + e.getMessage());
        }
    }
    
